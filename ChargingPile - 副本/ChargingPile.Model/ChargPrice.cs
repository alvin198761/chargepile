using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ChargingPile.Model
{
    /// <summary>
    /// 类CFG_CHARGPRICE。
    /// </summary>
    [Serializable]
    public class ChargPrice
    {
        /// <summary>
        /// 唯一Id
        /// </summary>
        public string Id { get; set; }

        /// <summary>
        /// 名称
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// 价格类型
        /// </summary>
        public string PriceType { get; set; }

        /// <summary>
        /// 版本号
        /// </summary>
        public decimal? Version { get; set; }

        /// <summary>
        /// 开始时间
        /// </summary>
        public DateTime? BeginDT { get; set; }

        /// <summary>
        /// 结束时间
        /// </summary>
        public DateTime? EndDT { get; set; }

        /// <summary>
        /// 价格
        /// </summary>
        public decimal? Price { get; set; }

        /// <summary>
        /// 创建时间
        /// </summary>
        public DateTime? CreateDT { get; set; }

        /// <summary>
        /// 更新时间
        /// </summary>
        public DateTime? UpdateDT { get; set; }

        public override string ToString()
        {
            return "ID:'" + Id + "',Name:'" + Name + "',PriceType:'" + PriceType
                + "',Version:'" + Version + "',BeginDT:'" + BeginDT + "',EndDT:'" +
                EndDT + "',Price:'" + Price + "',CreateDT:'" + CreateDT +
                "',UpdateDT:'" + UpdateDT + "'";
        }
    }
}
